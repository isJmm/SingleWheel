#include "zf_common_debug.h"

#include "zf_device_key.h"

static uint32               scanner_period = 0;                                 // 鎸夐敭鐨勬壂鎻忓懆鏈�
static uint32               key_press_time[KEY_NUMBER];                         // 鎸夐敭淇″彿鎸佺画鏃堕暱
 key_state_enum       key_state[KEY_NUMBER];                              // 鎸夐敭鐘舵��

static const gpio_pin_enum  key_index[KEY_NUMBER] = KEY_LIST;                   // 鎸夐敭鍒楄〃

//-------------------------------------------------------------------------------------------------------------------
// 鍑芥暟绠�浠�     鎸夐敭鐘舵�佹壂鎻�
// 鍙傛暟璇存槑     void
// 杩斿洖鍙傛暟     void
// 浣跨敤绀轰緥     key_scanner();
// 澶囨敞淇℃伅     杩欎釜鍑芥暟鏀惧湪涓诲惊鐜垨鑰� PIT 涓柇涓�
//-------------------------------------------------------------------------------------------------------------------
void key_scanner (void)
{
    uint8 i = 0;
    for(i = 0; KEY_NUMBER > i; i ++)
    {
        if(KEY_RELEASE_LEVEL != gpio_get_level(key_index[i]))                   // 鎸夐敭鎸変笅
        {
            key_press_time[i] ++;
            if(KEY_LONG_PRESS_PERIOD / scanner_period <= key_press_time[i])
            {
                key_state[i] = KEY_LONG_PRESS;
            }
        }
        else                                                                    // 鎸夐敭閲婃斁
        {
            if((KEY_LONG_PRESS != key_state[i]) && (KEY_MAX_SHOCK_PERIOD / scanner_period <= key_press_time[i]))
            {
                key_state[i] = KEY_SHORT_PRESS;
            }
            else
            {
                key_state[i] = KEY_RELEASE;
            }
            key_press_time[i] = 0;
        }
    }
}

//-------------------------------------------------------------------------------------------------------------------
// 鍑芥暟绠�浠�     鑾峰彇鎸夐敭鐘舵��
// 鍙傛暟璇存槑     key_n           鎸夐敭绱㈠紩
// 杩斿洖鍙傛暟     key_state_enum  鎸夐敭鐘舵��
// 浣跨敤绀轰緥     key_get_state(KEY_1);
// 澶囨敞淇℃伅
//-------------------------------------------------------------------------------------------------------------------
key_state_enum key_get_state (key_index_enum key_n)
{
    return key_state[key_n];
}

//-------------------------------------------------------------------------------------------------------------------
// 鍑芥暟绠�浠�     娓呴櫎鎸囧畾鎸夐敭鐘舵��
// 鍙傛暟璇存槑     key_n           鎸夐敭绱㈠紩
// 杩斿洖鍙傛暟     void            鏃�
// 浣跨敤绀轰緥     key_clear_state(KEY_1);
// 澶囨敞淇℃伅
//-------------------------------------------------------------------------------------------------------------------
void key_clear_state (key_index_enum key_n)
{
    key_state[key_n] = KEY_RELEASE;
}

//-------------------------------------------------------------------------------------------------------------------
// 鍑芥暟绠�浠�     娓呴櫎鎵�鏈夋寜閿姸鎬�
// 鍙傛暟璇存槑     void            鏃�
// 杩斿洖鍙傛暟     void            鏃�
// 浣跨敤绀轰緥     key_clear_all_state();
// 澶囨敞淇℃伅
//-------------------------------------------------------------------------------------------------------------------
void key_clear_all_state (void)
{
    key_state[0] = KEY_RELEASE;
    key_state[1] = KEY_RELEASE;
    key_state[2] = KEY_RELEASE;
    key_state[3] = KEY_RELEASE;
}

//-------------------------------------------------------------------------------------------------------------------
// 鍑芥暟绠�浠�     鎸夐敭鍒濆鍖�
// 鍙傛暟璇存槑     period          鎸夐敭鎵弿鍛ㄦ湡 浠ユ绉掍负鍗曚綅
// 杩斿洖鍙傛暟     void
// 浣跨敤绀轰緥     key_init(10);
// 澶囨敞淇℃伅
//-------------------------------------------------------------------------------------------------------------------
void key_init (uint32 period)
{
    zf_assert(0 < period);
    uint8 loop_temp = 0; 
    for(loop_temp = 0; KEY_NUMBER > loop_temp; loop_temp ++)
    {
        gpio_init(key_index[loop_temp], GPI, GPIO_HIGH, GPI_PULL_UP);
        key_state[loop_temp] = KEY_RELEASE;
    }
    scanner_period = period;
}

uint8 key_switch(void)
{
    uint8 i = 0;
    for(i = 0; KEY_NUMBER > i; i ++)
    {
        if(key_state[i] != KEY_RELEASE)                   // 鎸夐敭鎸変笅
        {
            return 1;
        }
    }
    return 0;
}
